package de.gematik.dsr.gms.application.ios.receipt.validation;

import jakarta.ws.rs.HeaderParam;
import jakarta.ws.rs.POST;
import jakarta.ws.rs.Path;
import org.eclipse.microprofile.rest.client.inject.RegisterRestClient;

/** Rest client for getting new receipts from Apple */
@Path("/v1/attestationData")
@RegisterRestClient(configKey = "apple-receipt")
public interface AppleReceiptRestClient {

  @POST
  byte[] getReceipt(@HeaderParam("Authorization") String authorization, byte[] body);
}
